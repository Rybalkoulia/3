#include <iostream>
using namespace std;

int main() {
    // Введення значень змінних
    double A, B, C;
    cout << "Введіть значення A, B, C: ";
    cin >> A >> B >> C;

    // Перевірка, чи значення впорядковані за зростанням
    if (A <= B && B <= C) {
        // Подвоюємо значення
        A *= 2;
        B *= 2;
        C *= 2;
    } else {
        // Змінюємо на протилежне
        A = -A;
        B = -B;
        C = -C;
    }

    // Виведення нових значень
    cout << "Нові значення змінних:\n";
    cout << "A = " << A << "\n";
    cout << "B = " << B << "\n";
    cout << "C = " << C << "\n";

    return 0;
}
